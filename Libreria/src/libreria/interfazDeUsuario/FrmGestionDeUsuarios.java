    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        boolean aux=true;
        for (int i = 0; i < tbUsuarios.getModel().getRowCount(); i++) {
            if(txtNombre.getText().equals(tbUsuarios.getModel().getValueAt(i, 0))){
                    aux=false;
            JOptionPane.showMessageDialog(null,"Usuario ya en existencia","ERROR",JOptionPane.ERROR_MESSAGE);
                    break;
                }
            }
            String nombre = txtNombre.getText(), nombreUsuario =txtNombreUsuario.getText(), contrasenia = txtContrasenia.getText();
            if(nombre.length()<2 || nombreUsuario.length()<3 || contrasenia.length() < 3){
                JOptionPane.showMessageDialog(null,"Rellene los campos solicitados","ERROR",JOptionPane.ERROR_MESSAGE);
                aux=false;
            }
        if(aux){
            try{
                ManejadorArchivos.agregarObjeto("Usuarios.poo", new Usuario(nombre,nombreUsuario,contrasenia));
                DefaultTableModel modelo = (DefaultTableModel)tbUsuarios.getModel();
                modelo.addRow(new Object[]{nombre,nombreUsuario});
                tbUsuarios.setModel(modelo);            
            } catch(ClassNotFoundException ex){
                System.out.println("when");
            }    
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

